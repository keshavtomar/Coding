name = "sAchIn ChaUDhaRy"
#len() function
length=len(name)#len function count number of character and spaces also
print(length)
#lower() method
lower=name.lower()#it change all alphabet in small letters
print(lower)
#upper() method
upper=name.upper()#it change all alphabet in capital letters
print(upper)
#title() method
title=name.title()#it will change first letter into upper case
print(title)
#count() method
count=name.count("h")#it will count the character repetition 
print(count)
#() after each method  can be put at the time of variable declaration as well as print statement
